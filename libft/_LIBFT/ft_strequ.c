#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	size_t	i;

	i = 0;
	if (!s1 && !s2)
		return (1);
	if (!s1 || !s2)
		return (0);
	while (s1[i] && s2[i])
	{
		if (ft_strcmp(s1, s2) != 0)
			return (0);
		i++;
	}
	return (1);
}
