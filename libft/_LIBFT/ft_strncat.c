#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t i;
	size_t x;

	x = 0;
	while (s1[x])
		x++;
	i = 0;
	while (i < n && s2[i])
		s1[x++] = s2[i++];
	s1[x] = '\0';
	return (s1);
}
